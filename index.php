				</div>
				<div class="col-md-6">
					<p class="lead">This project started with an alarming observation: <span class="highlight">all of my female friends</span> (including me) had experienced some sort of gender based violence (from sexual harassment at work, in the street, in public transportation, to sexual abuse). Not only was this observation depressing, it was also infuriating.<br>
					I knew I had to do something about it. I wanted to <span class="highlight">end rape culture.</span><br></p>
					<p class="lead description">I wanted to have conversations with men about toxic masculinities. I wanted to reflect on my own femininity and its toxicity.
					I wanted to know what kind of man would make the world a better place. I wanted to explore gender theories as well as binary concepts like male and female energies.
					I knew these conversations would get tense. I knew some listeners might be reluctant to the topics. In the same time, I did not want to have a conversation with like-minded people.
					I wanted to reach white educated men as I believe that they are part of the solution to make this world more equal.
					In other words: we need them on board.<br><br>
					So I worked for two years and came up with: <br>
					<br>
					The Gap, a non-fiction podcast series which explores the differences and the similarities between men and women. It follows my own thought process on feminism, toxic masculinities, gender roles and male and female energies.
					Techniques like <span class="highlight">ASMR</span> (Autonomous Sensory Meridian Response) triggers and <span class="highlight">binaural audio</span> are used in order to create a relaxation state, so that we can all truly listen to each other. This series aims to spark a genuine conversation around these sensitive topics.
					<br>The series was released in August 2018. You can listen to it <a href="https://soundcloud.com/user-957436759" target="_blank">here</a><br>
					And find all the info about The Gap <a href="https://www.thegappodcast.com/" target="_blank">here</a>.
